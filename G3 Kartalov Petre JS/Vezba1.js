function sum(x, y) {
  return x + y;
}
console.log(`x + y equals ${sum(4, 2)}`);

function subtract(x, y) {
  return x - y;
}
console.log(`x - y equals ${subtract(4, 2)}`);

function multiply(x, y) {
  return x * y;
}
console.log(`x * y equals ${multiply(4, 2)}`);

function divide(x, y) {
  return x / y;
}
console.log(`x / y equals ${divide(4, 2)}`);

function suma(x, y, j) {
  return x + y + j;
}
console.log(suma(1, 4));
